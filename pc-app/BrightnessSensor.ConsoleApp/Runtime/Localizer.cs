namespace BrightnessSensor.ConsoleApp.Runtime;

internal sealed class Localizer(UiLanguage language)
{
    private readonly UiLanguage _language = language;

    public string this[string key] => Translate(key);

    public string Translate(string key)
    {
        return _language switch
        {
            UiLanguage.Russian => Russian.TryGetValue(key, out var value) ? value : English[key],
            UiLanguage.Spanish => Spanish.TryGetValue(key, out var value) ? value : English[key],
            _ => English[key]
        };
    }

    public static IReadOnlyCollection<string> RequiredKeys => English.Keys;

    public static bool HasTranslation(UiLanguage language, string key)
    {
        return language switch
        {
            UiLanguage.Russian => Russian.ContainsKey(key),
            UiLanguage.Spanish => Spanish.ContainsKey(key),
            _ => English.ContainsKey(key)
        };
    }

